public class Faktoriyel {
    public static void main(String[] args) {
        // Faktöriyel hesabı: 5 * 4 * 3 * 2 * 1 = 120
        int faktoriyelSonucu = 5 * 4 * 3 * 2 * 1;
        
        // Ekrana hem işlemi hem de sonucu yazdır.
        System.out.println("5! = 5 x 4 x 3 x 2 x 1"); 
        System.out.println("5! Sonucu: " + faktoriyelSonucu);
    }
}