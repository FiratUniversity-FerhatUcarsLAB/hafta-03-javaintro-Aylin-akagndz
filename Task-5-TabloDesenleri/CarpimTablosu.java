public class CarpimTablosu {
    public static void main(String[] args) {
        // Çarpımını bulacağımız sayı: 2
        int carpilanSayi = 2;
        
        System.out.println("--- 2'lerin Çarpım Tablosu ---");
        
        // 1'den 10'a kadar olan değerler için çarpım tablosunu yazdır
        // (Döngü kullanmadan, manuel olarak yazdırıyoruz)
        System.out.println(carpilanSayi + " x 1  = " + (carpilanSayi * 1));
        System.out.println(carpilanSayi + " x 2  = " + (carpilanSayi * 2));
        System.out.println(carpilanSayi + " x 3  = " + (carpilanSayi * 3));
        System.out.println(carpilanSayi + " x 4  = " + (carpilanSayi * 4));
        System.out.println(carpilanSayi + " x 5  = " + (carpilanSayi * 5));
        System.out.println(carpilanSayi + " x 6  = " + (carpilanSayi * 6));
        System.out.println(carpilanSayi + " x 7  = " + (carpilanSayi * 7));
        System.out.println(carpilanSayi + " x 8  = " + (carpilanSayi * 8));
        System.out.println(carpilanSayi + " x 9  = " + (carpilanSayi * 9));
        System.out.println(carpilanSayi + " x 10 = " + (carpilanSayi * 10));
    }
}