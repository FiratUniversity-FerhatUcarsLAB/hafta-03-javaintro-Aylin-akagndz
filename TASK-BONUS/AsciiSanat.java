public class AsciiSanat {
    public static void main(String[] args) {
        System.out.println("--- AYLİN Adının ASCII Sanatı ---");
        
        // ----------------- A HARFİ -----------------
        System.out.println("      /\\      ");
        System.out.println("     /  \\     ");
        System.out.println("    /____\\    ");
        System.out.println("   /      \\   ");
        System.out.println("  /        \\  ");
        
        // ----------------- Y HARFİ -----------------
        System.out.println("");
        System.out.println(" \\ /  ");
        System.out.println("  V   ");
        System.out.println("  |   ");
        System.out.println("  |   ");
        
        // ----------------- L HARFİ -----------------
        System.out.println("");
        System.out.println(" L    ");
        System.out.println(" L    ");
        System.out.println(" L    ");
        System.out.println(" LLLL ");

        // ----------------- İ HARFİ -----------------
        System.out.println("");
        System.out.println(" . ");
        System.out.println(" | ");
        System.out.println(" | ");
        System.out.println(" | ");

        // ----------------- N HARFİ -----------------
        System.out.println("");
        System.out.println(" N\\N  ");
        System.out.println(" N \\N ");
        System.out.println(" N  N ");
        System.out.println(" N  N ");
    }
}